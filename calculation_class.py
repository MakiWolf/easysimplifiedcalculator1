def add(number_1, number_2, result = 0):
	result = float(number_1) + float(number_2)
	return (result)
def sub(number_1, number_2, result = 0):
	result = float(number_1) - float(number_2)
	return (result)
def mult(number_1, number_2, result = 0):
	result = float(number_1) * float(number_2)
	return (result)
def div(number_1, number_2, result = 0):
	result = float(number_1) / float(number_2)
	return (result)
